void solve(){
	i64 n, m;
	cin >> n >> m;

	i64 ans = 0;
	// case b = 1 is separate
	// if b = 1 then we just need 1 | a ==> n options for a
	ans += n;

	// case b >= 2
	for (i64 b = 2; b <= m; b++){
		for (i64 k = b - 1; k <= n / b; k += b){
			++ans;
		}
	}
	cout << ans << endl;
}
 
