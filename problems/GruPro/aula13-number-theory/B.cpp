void solve(){
    map<i64, i64> cnt;
    map<i64, i64> last;
    i64 q;
    cin >> q;

    auto find_mex = [&last, &cnt](i64 k){
        i64 final_k = last[k];
        while (cnt.find(final_k) != cnt.end()){
            final_k += k;
        }
        last[k] = final_k;
        return final_k;
    };
    ++cnt[0];

    for (i64 i = 0; i < q; i++){
        string mode;
        i64 x, k;
        cin >> mode;
        if (mode == "+"){
            cin >> x;
            ++cnt[x];
        } else {
            cin >> k;
            cout << find_mex(k) << endl;
        }
    }
}
 
