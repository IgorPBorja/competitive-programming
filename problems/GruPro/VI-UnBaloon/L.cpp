i64 f(i64 x, i64 a, i64 b, i64 c){
    i64 x2 = x * x, x3 = x2 * x;
    return abs(
        (a + c) * x3 - b * x2 + b * c * x + (x + c) * (x - a)
    );
}

void solve(){
    i64 a, b, c;
    cin >> a >> b >> c;

    i64 q, x, res = 0;
    cin >> q;
    for (i64 i = 0; i < q; i++) {
        cin >> x;
        res ^= f(x, a, b, c);
    }
    cout << res << endl;
}
 
