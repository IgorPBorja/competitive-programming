template <typename ForwardIterator>
void read(ForwardIterator itr, int n)
{
    for (int i = 0; i < n; i++)
    {
        cin >> *itr;
        ++itr;
    }
}
template <typename ForwardIterator>
void print(ForwardIterator itr, int n, string sep=" ")
{
    for (int i = 0; i < n; i++)
    {
        cout << *itr << sep;
        ++itr;
    }
}
template <typename ForwardIterator, typename T>
void setval(ForwardIterator itr, int n, T v)
{
    for (int i = 0; i < n; i++)
    {
        *itr = v;
        ++itr;
    }
}

int main(){
    int n, x;
    cin >> n >> x;
    int a[n];
    read(a, n);
    sort(a, a + n);
    int cnt = 0;
    int l = 0, r = n - 1;
    while (l < r){
        if (a[r] + a[l] > x){
            --r;
            ++cnt;
        } else{
            --r;
            ++l;
            ++cnt;
        }
    }
    if (l == r) ++cnt;

    cout << cnt;
}
